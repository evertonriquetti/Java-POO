public class Pessoa {
    //Atributos
    String nome;
    int idade;
    String sexo;
    
    //Métodos
    public void fazerAniversario(){
        this.idade ++;
    }
    
    //Métodos Especiais

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }
    
}
